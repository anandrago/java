package defaultpackage;

public class $2IterationStatement {
public static void main(String[] args) {
	//while loop
	int i=1;
	while(i<=5)
	{
		System.out.println("while");
		i++;
	}
	
	//do while loop
	int j=1;
	do {
		System.out.println("do while");
		j++;
	}while(j<=5);
	
	//for loop
	for(i=1;i<=5;i++)
	{
		System.out.println("for loop");
	}	
}
}

